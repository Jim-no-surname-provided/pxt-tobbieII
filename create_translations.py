def write_json(path: Path, data: dict[str, str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")


def main() -> None:
    output_root = Path("_locales")
    output_root.mkdir(parents=True, exist_ok=True)

    for locale in LOCALES:
        locale_dir = output_root / locale

        translation = TRANSLATIONS.get(locale)
        jsdoc_translation = JSDOC_TRANSLATIONS.get(locale)

        if translation is not None:
            write_json(
                locale_dir / "TobbieII-strings.json",
                translation
            )
        else:
            print(f"no translate for language {locale}")
        if jsdoc_translation is not None:
            write_json(
                locale_dir / "TobbieII-jsdoc-strings.json",
                jsdoc_translation
            )
        else :
            print(f"no jsdoc translation for language {jsdoc_translation}")

    print(f"Created locale folders in: {output_root.resolve()}")


if __name__ == "__main__":
    main()
